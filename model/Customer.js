const mongoose = require("mongoose");

const customerSchema = new mongoose.Schema({
    fname: {
        type: String,
        required: true
    },
    lname: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    phone: {
        type: String,
        required: true
    },
})

const Customer = mongoose.model("customers", customerSchema);

module.exports = Customer;

